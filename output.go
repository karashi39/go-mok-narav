package main

import (
    "fmt"
)

func clear_row () {
    // stdoutのカーソルを1行上に戻す
    fmt.Print("\033[1A\033[K")
}

func display (board [][]string) {
    for i :=0; i < len(board); i++ {
        clear_row()
    }
    for i := 0; i < len(board); i++ {
        for j := 0; j < len(board[i]); j++ {
            fmt.Printf("%s ", board[i][j])
        }
        fmt.Println()
    }
}

